package edu.dosw.lab.Pixel_Scribe.model;

/**
 * Enum para roles de usuario en el sistema.
 * 
 * <p>Roles disponibles:</p>
 * <ul>
 *   <li>USER - Usuario normal que puede subir y ver sus imágenes</li>
 *   <li>ADMIN - Administrador con permisos adicionales</li>
 * </ul>
 */
public enum Role {
    /**
     * Usuario estándar del sistema
     */
    USER,
    
    /**
     * Administrador del sistema
     */
    ADMIN
}


